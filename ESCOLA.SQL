SHOW DATABASES;
CREATE DATABASE ESCOLA;
USE ESCOLA;
CREATE TABLE ALUNO(
	ID_ALUNO INT PRIMARY KEY UNIQUE AUTO_INCREMENT NOT NULL,
    NOME_ALUNO VARCHAR (120) NOT NULL,
    CPF_ALUNO INT (11) NOT NULL,
    RG_ALUNO INT (9) NOT NULL,
    RA INT (10) NOT NULL,
    EMAIL_ALUNO VARCHAR (120) NOT NULL,
    TELEFONE_ALUNO INT (11) NOT NULL,
    DATA_NASCIMENTO_ALUNO DATE NOT NULL,
    ENDERECO_ALUNO VARCHAR (120) NOT NULL
);
SELECT * FROM ALUNO;
CREATE TABLE EMPRESA(
	ID_EMPRESA INT PRIMARY KEY UNIQUE AUTO_INCREMENT NOT NULL,
    CNPJ INT (14) NOT NULL,
    RAZAO_SOCIAL VARCHAR (120) NOT NULL,
    ENDERECO_EMPRESA VARCHAR (120) NOT NULL,
    SITE VARCHAR (120) NOT NULL,
    TELEFONE_EMPRESA INT (11) NOT NULL,
    EMAIL_EMPRESA VARCHAR (120) NOT NULL,
    INSC_MUNICIPAL INT (16) NOT NULL,
    INSC_ESTADUAL INT (20) NOT NULL
);
SELECT * FROM EMPRESA;
SHOW TABLES;
CREATE TABLE FUNCIONARIO(
	ID_FUNCIONARIO INT PRIMARY KEY UNIQUE AUTO_INCREMENT NOT NULL,
    NOME_FUNC VARCHAR (120) NOT NULL,
    CPF_FUNC INT (11) NOT NULL,
    RG_FUNC INT (9) NOT NULL,
    TELEFONE_FUNC INT (11) NOT NULL,
    EMAIL_FUNC VARCHAR (120) NOT NULL,
    ENDERECO_FUNC VARCHAR (120) NOT NULL,
    SALARIO DECIMAL (10,2) NOT NULL,
    FUNCAO VARCHAR (120) NOT NULL,
    DATA_NASCIMENTO_FUNC DATE NOT NULL
);
CREATE TABLE CURSO(
	ID_CURSO INT PRIMARY KEY UNIQUE AUTO_INCREMENT NOT NULL,
    DS VARCHAR (120) NOT NULL,
    ADM VARCHAR (120) NOT NULL,
    RH VARCHAR (120) NOT NULL,
    ENFERMAGEM VARCHAR (120) NOT NULL
);
CREATE TABLE DISCIPLINA(
	ID_DISCIPLINA INT PRIMARY KEY UNIQUE AUTO_INCREMENT NOT NULL,
    BD_I VARCHAR (120) NOT NULL,
	PW_I VARCHAR (120) NOT NULL,
    PROG_ALG VARCHAR (120) NOT NULL,
    ETICA VARCHAR (120) NOT NULL,
    INGLES VARCHAR (120) NOT NULL,
    DESIGN_DIGITAL VARCHAR (120) NOT NULL,
    FUND_INFO VARCHAR (120) NOT NULL,
    OSA VARCHAR (120) NOT NULL
);
CREATE TABLE FINANCA(
	ID_FINANCA INT PRIMARY KEY UNIQUE AUTO_INCREMENT NOT NULL,
    LUZ DECIMAL (10,2) NOT NULL,
    AGUA DECIMAL (10,2) NOT NULL,
    INTERNET DECIMAL (10,2) NOT NULL,
    IPTU DECIMAL (10,2) NOT NULL
);
